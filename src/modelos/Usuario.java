package modelos;

public class Usuario {

    private String usuario;
    private String contasenia;
    private String rol;
    private boolean activo;

    public String getUsuario() {
        return usuario;
    }

    public String getContasenia() {
        return contasenia;
    }

    public String getRol() {
        return rol;
    }

    public boolean isActivo() {
        return activo;
    }
}
